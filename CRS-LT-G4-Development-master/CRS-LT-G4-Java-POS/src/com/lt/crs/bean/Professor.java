package com.lt.crs.bean;

/**
 * @author Naman,Purnima,Radha,Ramit,Venisraj,Vignesh
 * Bean of professor to store professor details
 */
public class Professor {
	private int professorId;
	private int professorName;
	/**
	 * @return the professorId
	 */
	public int getProfessorId() {
		return professorId;
	}
	/**
	 * @param professorId the professorId to set
	 */
	public void setProfessorId(int professorId) {
		this.professorId = professorId;
	}
	/**
	 * @return the professorName
	 */
	public int getProfessorName() {
		return professorName;
	}
	/**
	 * @param professorName the professorName to set
	 */
	public void setProfessorName(int professorName) {
		this.professorName = professorName;
	}
	
}
